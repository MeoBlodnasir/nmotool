#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	int i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && n--)
		i++;
	return (s1[i] == s2[i] || !n ? 1 : 0);
}
