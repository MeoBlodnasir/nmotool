#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*a;
	int				i;

	a = b;
	i = 0;
	while (len)
	{
		a[i] = c;
		len--;
		i++;
	}
	return (b);
}
